# Q1. Write a Python program to print a formatted string using print() and f-string. 

# Program to print a formatted string using print() and f-string

# Variables
name = "Swastik"
age = 21
course = "Python Programming"

# Using print() with format()
print("Hello, my name is {}. I am {} years old and I am learning {}.".format(name, age, course))

# Using f-string
print(f"Hello, my name is {name}. I am {age} years old and I am learning {course}.")


